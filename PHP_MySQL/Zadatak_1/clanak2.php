            <h1>Novi prvak - Thierry Neuville! (Galerija)</h1>
            <p>24. studeni 2025.</p>
            
            <p>Nakon 13 uzbudljivih utrka, Thierry Neuville okrunjen je prvakom! Od Monte Carla, do Japana zadržao je prvo mjesto u ukupnom poretku.</p>
            <p>Prijetili su i Sebastien Ogier, koji je pred kraj sezone odabrao voziti full-time, kako bi spasio Toyotine šanse za titulu vozača, te njegov 
                timski kolega Ott Tanak. Naposlijetku, Tanak je ostao kao jedina i najveća konkurencija no greška na samom kraju Rally Japana koštala ga je 
                titule vozača a Hyundai titule proizvođača.</p>
            <p>Veliki uspijeh proslavio je uz suvozača Martijna Wydaeghea te drugu Hyundai-evu posadu, Mikkelsen-Erikssen. Poznato je kako su Thierry i Andreas
               i inače dobri prijatelji pa se vidjela iskrena radost. S druge strane, Ott Tanak razočarano je sjedio u blizini slupanog i20N Rally1 Hybrida.</p>
            <p>Borba za titulu prvaka proizvođača odvijala se sve do posljednjeg brzinskog ispita. Ogier je briljirao i postavio najbrže vrijeme Powerstage-a dok je Evans odnio pobjedu.</p>
            <p>Iduće sezone čeka nas još zanimljivije prvenstvo obzirom da se dvostruki svjetski prvak Kalle Rovanpera vraća, te će voziti punu sezonu. Može li
                Thierry Neuville obraniti titulu? Hoćemo li vidjeti novog prvaka kao Elfyna Evansa ili Adriena Fourmauxa? Hoćemo li gledati nove/stare prvake Rovanperu ili Tanaka?
                Saznat ćemo uskoro u Monacu! </p>

